#/bin/bash
set -e

#--------------------------------------------------------------
# k3d
# https://github.com/k3d-io/k3d
#--------------------------------------------------------------
K3D_VERSION=v5.0.0

#--------------------------------------------------------------
# k3d
# https://github.com/k3d-io/k3d
#--------------------------------------------------------------
echo  "#-----------------------------------------"
echo  "# Install: k3d"
echo  "#-----------------------------------------"
if [ -z "$(command -v k3d)" ]; then
    curl https://raw.githubusercontent.com/k3d-io/k3d/main/install.sh | TAG=${K3D_VERSION} bash
    k3d completion bash > /usr/local/etc/bash_completion.d/k3d
fi
#--------------------------------------------------------------
# Version
#--------------------------------------------------------------
echo  "#-----------------------------------------"
echo  "# Version: k3d "
echo  "#-----------------------------------------"
k3d version
